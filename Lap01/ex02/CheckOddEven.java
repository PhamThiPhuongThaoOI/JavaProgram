package Lap01.ex02;

public class CheckOddEven {
    public static void main(String[] args) {
        int number = 49;
        System.out.println( " The number is " + number);

        if ( number %2 ==0 ) {
            System.out.println( " This is even number ");
        }else {
            System.out.println( " This is old number ");
        }
        System.out.println( " Bye!");
    }
}
